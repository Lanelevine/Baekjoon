package org.opentutorials.javatutorials.operator;

public class ArithmeticDemo {

	public static void main(String[] args) {
		// resuit의 값은 3
		int resuit = 1+2;
		System.out.println(resuit);
		
		
		// resuit의 값은 2
		resuit = resuit - 1;
		System.out.println(resuit);
		
		// resuit의 값은 4
		resuit = resuit * 2;
		System.out.println(resuit);
		
		// resuit의 값은 2
		resuit = resuit / 2;
		System.out.println(resuit);
		
		// resuit의 값은 10
		resuit = resuit + 8;
		System.out.println(resuit);
		
		// RESUIT의 값은 3
		resuit = resuit % 7;
		System.out.println(resuit);
	}

}
